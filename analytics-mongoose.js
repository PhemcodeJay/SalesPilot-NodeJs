const mongoose = require('mongoose');
const Schema = mongoose.Schema;

// ✅ Categories Schema (Added most sold product tracking)
const CategorySchema = new Schema({
  category_name: { type: String, required: true },
  description: { type: String },
  created_at: { type: Date, default: Date.now },
  most_sold_product_id: { type: mongoose.Schema.Types.ObjectId, ref: 'Product' }, // Track best-selling product per category
  total_sales: { type: Number, default: 0 }, // Total sales per category
  revenue: { type: Number, default: 0 } // Total revenue per category
});

// ✅ Products Schema (Added sales analytics per product)
const ProductSchema = new Schema({
  name: { type: String, required: true },
  description: { type: String },
  price: { type: Number, required: true },
  cost: { type: Number, required: true },
  category_id: { type: mongoose.Schema.Types.ObjectId, ref: 'Category', required: true },
  created_at: { type: Date, default: Date.now },
  stock_qty: { type: Number, default: 0 },
  supply_qty: { type: Number, default: 0 },
  image_path: { type: String },
  product_type: { type: String, enum: ['Goods', 'Services', 'Digital'], default: 'Goods' },
  staff_name: { type: String },
  category: { type: String },
  inventory_qty: { type: Number, default: function () { return this.stock_qty + this.supply_qty; } },
  profit: { type: Number, default: function () { return this.price - this.cost; } },
  total_sales: { type: Number, default: 0 }, // Total number of times this product has been sold
  revenue_generated: { type: Number, default: 0 }, // Total revenue generated by this product
  last_sold_date: { type: Date } // Last time this product was sold
});

// ✅ Inventory Schema (Added tracking for most stocked product)
const InventorySchema = new Schema({
  product_id: { type: mongoose.Schema.Types.ObjectId, ref: 'Product', required: true },
  stock_qty: { type: Number, default: 0 },
  supply_qty: { type: Number, default: 0 },
  sales_qty: { type: Number, default: 0 },
  available_stock: { type: Number, default: function () { return this.stock_qty + this.supply_qty - this.sales_qty; } },
  inventory_qty: { type: Number, default: function () { return this.stock_qty + this.supply_qty; } },
  last_updated: { type: Date, default: Date.now },
  most_stocked_product_id: { type: mongoose.Schema.Types.ObjectId, ref: 'Product' }, // Track the most stocked product
  least_stocked_product_id: { type: mongoose.Schema.Types.ObjectId, ref: 'Product' } // Track the least stocked product
});

// ✅ Sales Schema (Added tracking for most sold products and staff performance)
const SalesSchema = new Schema({
  product_id: { type: mongoose.Schema.Types.ObjectId, ref: 'Product', required: true },
  user_id: { type: mongoose.Schema.Types.ObjectId, ref: 'User', required: true },
  customer_id: { type: mongoose.Schema.Types.ObjectId, ref: 'Customer', required: true },
  staff_id: { type: mongoose.Schema.Types.ObjectId, ref: 'Staff', required: true },
  sales_qty: { type: Number, required: true },
  sale_date: { type: Date, default: Date.now },
  sale_status: { type: String, enum: ['completed', 'pending'], default: 'pending' },
  payment_status: { type: String, enum: ['completed', 'due', 'paid'], default: 'due' },
  name: { type: String },
  product_type: { type: String, enum: ['Goods', 'Services', 'Digital'], required: true },
  sale_note: { type: String },
  sales_price: { type: Number, required: true },
  total_price: { type: Number, default: function () { return this.sales_qty * this.sales_price; } },
  most_sold_product_id: { type: mongoose.Schema.Types.ObjectId, ref: 'Product' }, // Most sold product in the period
  sales_of_the_month: { type: Number, default: 0 }, // Total sales for the current month
  staff_of_the_month_id: { type: mongoose.Schema.Types.ObjectId, ref: 'Staff' } // Best-performing staff of the month
});

// ✅ Customers Schema (Added most active customer tracking)
const CustomerSchema = new Schema({
  customer_name: { type: String, required: true },
  customer_email: { type: String, required: true },
  customer_phone: { type: String, required: true },
  customer_location: { type: String },
  created_at: { type: Date, default: Date.now },
  total_purchases: { type: Number, default: 0 }, // Total number of purchases
  last_purchase_date: { type: Date }, // Last purchase date
  customer_of_the_day: { type: Boolean, default: false } // Mark most active customer of the day
});

// ✅ Expenses Schema (Added weekly and monthly expense tracking)
const ExpenseSchema = new Schema({
  product_id: { type: mongoose.Schema.Types.ObjectId, ref: 'Product', required: true },
  expense_type: { type: String, enum: ['raw_material', 'transport', 'tax', 'other'], required: true },
  amount: { type: Number, required: true },
  expense_date: { type: Date, default: Date.now },
  description: { type: String },
  expenses_of_the_week: { type: Number, default: 0 }, // Total expenses for the week
  expenses_of_the_month: { type: Number, default: 0 } // Total expenses for the month
});

// ✅ Reports Schema (Added top-performing categories and products)
const ReportSchema = new Schema({
  report_date: { type: Date, required: true },
  revenue: { type: Number, required: true },
  profit_margin: { type: Number, required: true },
  revenue_by_product: { type: Object }, // Store as JSON
  total_sales: { type: Number, required: true },
  total_expenses: { type: Number, required: true },
  total_profit: { type: Number, required: true },
  created_at: { type: Date, default: Date.now },
  updated_at: { type: Date, default: Date.now },
  most_sold_product_id: { type: mongoose.Schema.Types.ObjectId, ref: 'Product' },
  most_profitable_product_id: { type: mongoose.Schema.Types.ObjectId, ref: 'Product' },
  top_category_id: { type: mongoose.Schema.Types.ObjectId, ref: 'Category' }
});

// ✅ Staff Schema (Added performance tracking)
const StaffSchema = new Schema({
  staff_name: { type: String, required: true },
  staff_email: { type: String, required: true },
  staff_phone: { type: String, required: true },
  position: { type: String, enum: ['manager', 'sales'], default: 'sales' },
  created_at: { type: Date, default: Date.now },
  total_sales_made: { type: Number, default: 0 }, // Track total sales made by the staff
  staff_of_the_month: { type: Boolean, default: false } // Mark the best-performing staff
});

// ✅ Sales Analytics Schema (Added category-based tracking)
const SalesAnalyticsSchema = new Schema({
  date: { type: Date, required: true },
  revenue: { type: Number, required: true },
  profit_margin: { type: Number, required: true },
  year_over_year_growth: { type: Number, required: true },
  cost_of_selling: { type: Number, required: true },
  total_sales: { type: Number, required: true },
  revenue_by_category: { type: Number, required: true },
  top_performing_category_id: { type: mongoose.Schema.Types.ObjectId, ref: 'Category' }
});

module.exports = {
  Category: mongoose.model('Category', CategorySchema),
  Product: mongoose.model('Product', ProductSchema),
  Inventory: mongoose.model('Inventory', InventorySchema),
  Sales: mongoose.model('Sales', SalesSchema),
  Customer: mongoose.model('Customer', CustomerSchema),
  Staff: mongoose.model('Staff', StaffSchema),
  Expense: mongoose.model('Expense', ExpenseSchema),
  Report: mongoose.model('Report', ReportSchema),
  SalesAnalytics: mongoose.model('SalesAnalytics', SalesAnalyticsSchema)
};
